import { Component, useState } from 'react';
import User from './User';

import classes from './Users.module.css';

class Users extends Component {
  constructor() {
    super();
    //in class bases comp, state is ALWAYS an object, here you group all of your state pieces together
    //no matter if these states are related or not
    this.state = {
      showUsers: true
    };
  }

  toggleUsersHandler() {
    // this.state.showUsers = false; --> WRONG
    // this.setState({ showUsers: false }); OR
    this.setState(curState => {
      return { showUsers: !curState.showUsers };
    });
  }

  render() {
    const usersList = (
      <ul>
        {this.props.users.map((user) => (
          <User key={user.id} name={user.name} />
        ))}
      </ul>
    );

    return (
      <div className={classes.users}>
        <button onClick={this.toggleUsersHandler.bind(this)}>
          {this.state.showUsers ? 'Hide' : 'Show'} Users
        </button>
        {this.state.showUsers && usersList}
      </div>)
  };
}

